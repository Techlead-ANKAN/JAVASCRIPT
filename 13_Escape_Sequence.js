// Escape Sequence - It is used to turn special characters into strings

// 1) \' - Single Quote
// 2) \" - Double Quote
// 3) \\ - Backslash
// 4) \b - Backspace (removes the previous character)
// 5) \f - Form Feed (break lines)
// 6) \n - New Line
// 7) \r - Carriage Return  (it will move the cursor back to the beginning of the line when it is printed)
// 8) \t - Horizontal Tabulator (provides a tab)
// 9) \v - Vertical Tabulator   (provides a vertivcal tab means break lines in other words)
